    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (idSeleccionado == -1) {
            lblResultado.setText("Seleccione una venta para actualizar.");
             return;
        }

        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "¿Desea actualizar esta venta?",
            "Confirmar actualización",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirmacion != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        Venta venta = obtenerVentaDesdeCampos(true);

        if (venta == null) {
        return;
        }

        VentaDAO ventaDAO = new VentaDAO();
        boolean actualizado = ventaDAO.actualizarVenta(venta);

        if (actualizado) {
            lblResultado.setText("Venta actualizada correctamente.");
            cargarTabla();
            limpiarCampos();
            tblVentas.clearSelection();
            idSeleccionado = -1;
        } else {
            lblResultado.setText("Error al actualizar la venta.");
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

