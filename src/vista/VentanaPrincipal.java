    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        Venta venta = obtenerVentaDesdeCampos(false);

        if (venta == null) {
            return;
        }

        VentaDAO ventaDAO = new VentaDAO();
        boolean guardado = ventaDAO.guardarVenta(venta);

        if (guardado) {
        lblResultado.setText("Venta registrada correctamente.");
        cargarTabla();
        limpiarCampos();
        tblVentas.clearSelection();
        } else {
            lblResultado.setText("Error al registrar la venta.");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

