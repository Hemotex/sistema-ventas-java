    private void cargarTabla() {

    VentaDAO ventaDAO = new VentaDAO();
    List<Object[]> lista = ventaDAO.obtenerVentas();

    DefaultTableModel modelo = new DefaultTableModel();

    modelo.addColumn("ID");
    modelo.addColumn("Fecha");
    modelo.addColumn("Precio");
    modelo.addColumn("Costo");
    modelo.addColumn("Cantidad");
    modelo.addColumn("Total");
    modelo.addColumn("Ganancia");

    tblVentas.setModel(modelo);

    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");

    for (Object[] fila : lista) {

        Object fechaObj = fila[1]; // La fecha está en la columna 1 (después del ID)

        String fechaFormateada = "";

        if (fechaObj instanceof LocalDate) {
            fechaFormateada = ((LocalDate) fechaObj).format(formatter);
        } else if (fechaObj instanceof java.sql.Date) {
            fechaFormateada = ((java.sql.Date) fechaObj).toLocalDate().format(formatter);
        }

        modelo.addRow(new Object[]{
            fila[0],                 // ID
            fechaFormateada,         // Fecha formateada
            fila[2],                 // Precio
            fila[3],                 // Costo
            fila[4],                 // Cantidad
            fila[5],                 // Total
            fila[6]                  // Ganancia
        });
    }
   }
    
    private Venta obtenerVentaDesdeCampos(boolean incluirId) {

    if (txtPrecio.getText().trim().isEmpty() ||
        txtCosto.getText().trim().isEmpty() ||
        txtCantidad.getText().trim().isEmpty()) {

        lblResultado.setText("Todos los campos son obligatorios.");
        return null;
    }

    double precio;
    double costo;
    int cantidad;

    try {
        precio = Double.parseDouble(txtPrecio.getText());
        costo = Double.parseDouble(txtCosto.getText());
        cantidad = Integer.parseInt(txtCantidad.getText());
    } catch (NumberFormatException e) {
        lblResultado.setText("Ingrese valores numéricos válidos.");
        return null;
    }

    if (precio <= 0 || costo <= 0 || cantidad <= 0) {
        lblResultado.setText("Los valores deben ser mayores a cero.");
        return null;
    }
    LocalDate fecha = LocalDate.now();
    if (incluirId) {
        return new Venta(idSeleccionado, precio, costo, cantidad, fecha);
    } else {
        return new Venta(precio, costo, cantidad, fecha);
    }
}
