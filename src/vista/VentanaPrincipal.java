    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int filaSeleccionada = tblVentas.getSelectedRow();

    if (filaSeleccionada == -1) {
        lblResultado.setText("Seleccione una venta para eliminar.");
        return;
    }

    int id = (int) tblVentas.getValueAt(filaSeleccionada, 0);

    VentaDAO ventaDAO = new VentaDAO();
    boolean eliminado = ventaDAO.eliminarVenta(id);

    if (eliminado) {
        lblResultado.setText("Venta eliminada correctamente.");
        cargarTabla();
    } else {
    lblResultado.setText("Error al eliminar la venta.");
}

    }//GEN-LAST:event_jButton3ActionPerformed

